function TurnOnWifi()
  Shell("wifi --on")
  Shell("wifi --load_firmware")
  Shell("bluetooth --on")
end

function TurnOnBaseBand()
  Shell("baseband --on")
  Shell("baseband --load_firmware")
end

function TurnOnAudio()
  Shell("audio -r")
end

function TurnOnAccel()
  Shell("sensor --sel accel --init")
end

function TurnOnCamera()
  Shell("camisp --pick front")
  Shell("camisp --on")
  Shell("camisp --preview on  ")
end

